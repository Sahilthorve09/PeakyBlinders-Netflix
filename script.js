const slider = document.querySelector('.slider');
const prev = document.querySelector('.prev');
const next = document.querySelector('.next');
const playPauseButtons = document.querySelectorAll('.play-pause');
const videos = document.querySelectorAll('.slide video');

let index = 0;
const totalSlides = videos.length - 2; // Show 3 at a time

// Update slider position
function updateSlider() {
    slider.style.transform = `translateX(-${index * (100 / 3)}%)`;
}

// Next button functionality
next.addEventListener('click', () => {
    if (index < totalSlides - 1) {
        index++;
        updateSlider();
    }
});

// Previous button functionality (fixed)
prev.addEventListener('click', () => {
    if (index > 0) {
        index--;
        updateSlider();
    }
});

// Play/Pause Button Functionality
playPauseButtons.forEach((button, i) => {
    button.addEventListener('click', () => {
        if (videos[i].paused) {
            videos[i].play();
            button.textContent = '❚❚'; // Pause icon
        } else {
            videos[i].pause();
            button.textContent = '►'; // Play icon
        }
    });

    videos[i].addEventListener('ended', () => {
        button.textContent = '►';
    });
});
